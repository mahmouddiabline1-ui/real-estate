export const formatPrice = (price, saleType) => {
  if (saleType === 'إيجار') {
    return `${price.toLocaleString('ar-EG')} جنيه/شهر`;
  }
  if (price >= 1000000) {
    return `${(price / 1000000).toFixed(1)} مليون جنيه`;
  }
  return `${price.toLocaleString('ar-EG')} جنيه`;
};
